       <h3 class="display text-center mt-4">Pedicura</h3>
       <p class="lead text-justify">Este tratamiento, no trata exclusivamente de las uñas de los pies, sino el cuidado integral de los pies.
      <br>
      Comenzamos con la limpieza de los pies, sumergiéndolos en un baño de agua y jabón. Seguidamente eliminamos las durezas, cortamos las uñas si fuese necesario y empujamos las cutículas, para acabar limando las uñas. Para dar el toque de hidratación y relajación, realizamos un masaje relajante con crema hidratante.
      <br>
      Terminamos esmaltando las uñas, con el color que tu elijas, con esmalte.  En el caso de que quieras potenciar la duración de tu esmaltado, te recomendamos la pedicura permanente conseguirás un acabado perfecto durante más de cuatro semanas.
      <br>
      La pedicura te ayudará a limpiar las células muertas que se acumulan en los pies, eliminar callosidades, y a evitar los talones agrietados y las durezas. Además, te permitirá poner a punto las uñas y cuidar tus cutículas.
